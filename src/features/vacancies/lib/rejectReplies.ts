/**
 * Шаблоны вежливого автоответа кандидату при отказе — свой текст под каждую причину.
 * Отправляется реальным сообщением в чат (company → candidate), поэтому кандидат
 * получает и уведомление (триггер БД на новое сообщение), и сам текст в переписке.
 *
 * ⚠️ Тексты пока курируются на фронте (мок). Позже можно вынести в БД (таблица
 * шаблонов на компанию) и редактировать в настройках — точка замены здесь одна.
 */

/** Причина отказа → завершающая фраза автоответа. */
const REASON_PHRASE: Record<string, string> = {
  'Недостаточно опыта под требования':
    'мы остановились на кандидатах с большим опытом под эти задачи',
  'Не совпали по навыкам': 'нам не хватило совпадения по ключевым навыкам вакансии',
  'Ожидания по зарплате выше бюджета': 'в этот раз мы не сошлись по зарплатным ожиданиям',
  'Выбрали другого кандидата': 'мы остановили выбор на другом кандидате',
  'Не подходит формат / локация': 'нам не подошёл предполагаемый формат работы или локация',
  'Другая причина': 'в этот раз мы решили не продолжать процесс',
}

const FALLBACK = REASON_PHRASE['Другая причина']

/** Собирает текст автоответа об отказе. */
export function buildRejectReply(reason: string, vacancyTitle: string, firstName?: string): string {
  const phrase = REASON_PHRASE[reason] ?? FALLBACK
  const hi = firstName ? `Здравствуйте, ${firstName}!` : 'Здравствуйте!'
  return (
    `${hi} Спасибо за отклик на вакансию «${vacancyTitle}» и за уделённое время. ` +
    `К сожалению, в этот раз мы не сможем продолжить — ${phrase}. ` +
    `Это не итоговая оценка вас как специалиста — будем рады видеть ваши отклики на наши будущие вакансии. Удачи в поиске!`
  )
}
