/**
 * Apple-набор эмодзи картинками (как в Telegram) — чтобы выглядели одинаково на всех
 * ОС. Берём с CDN jsDelivr (пакет `emoji-datasource-apple`), имя файла = кодпоинты
 * символа через дефис. Если картинки нет — компонент `Emoji` откатывается на
 * системный символ.
 */
const APPLE_BASE = 'https://cdn.jsdelivr.net/npm/emoji-datasource-apple@15.0.1/img/apple/64'

/** Кодпоинты эмодзи в виде `1f600` / `2764-fe0f` (как в именах файлов набора). */
export function emojiCodePoints(emoji: string): string {
  const pts: string[] = []
  for (const ch of emoji) {
    const cp = ch.codePointAt(0)
    if (cp) pts.push(cp.toString(16))
  }
  return pts.join('-')
}

export function appleEmojiUrl(emoji: string): string {
  return `${APPLE_BASE}/${emojiCodePoints(emoji)}.png`
}

