/**
 * Санитайзеры имён — удаляют недопустимые символы (эмодзи, спецсимволы) прямо
 * при вводе/вставке. Применяются на `onChange` полей имени в онбординге и редакторах.
 * Уже сохранённые профили не трогаются — это только валидация нового ввода.
 */

/** Имя человека: только русские буквы, пробел и дефис (напр. «Анна-Мария Иванова»). */
export function sanitizePersonName(value: string): string {
  return value.replace(/[^а-яёА-ЯЁ \-]/gu, '')
}

/**
 * Название компании: русские/латинские буквы, цифры, пробел и базовые символы
 * (`- . & '`) — чтобы работали бренды вроде «Kolibel», «1С», «Mail.ru», «H&M».
 */
export function sanitizeCompanyName(value: string): string {
  return value.replace(/[^а-яёА-ЯЁa-zA-Z0-9 \-.&']/gu, '')
}
