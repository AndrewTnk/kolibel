-- ============================================================
-- 0033_notification_prefs.sql
-- Настройки уведомлений по типам (раздел «Настройки → Уведомления»).
-- Храним как jsonb-карту { <kind>: bool } на profiles. Отсутствие ключа =
-- уведомление включено (значение по умолчанию — true). Енфорс клиентский:
-- отключённые типы не показываются в списке/тосте и не считаются в бейдже.
-- Применить: Supabase Dashboard → SQL Editor → вставить → Run.
-- ============================================================

alter table public.profiles
  add column if not exists notification_prefs jsonb not null default '{}'::jsonb;
